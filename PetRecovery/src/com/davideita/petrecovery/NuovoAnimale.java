public class NuovoAnimale {

    private String animale;
    private String NomeAnimale;

    /**
     * @return the animale
     */
    public String getAnimale() {
        return animale;
    }

    /**
     * @param animale the animale to set
     */
    public void setAnimale(String animale) {
        this.animale = animale;
    }

    /**
     * @return the animale
     */
    public String getNomeAnimale() {
        return NomeAnimale;
    }

    /**
     * @param animale the animale to set
     */
    public void setNomeAnimale(String animale) {
        this.NomeAnimale = animale;
    }

}
